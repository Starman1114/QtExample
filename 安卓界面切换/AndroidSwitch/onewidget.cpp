#include "onewidget.h"
#include "ui_onewidget.h"

OneWidget::OneWidget(QWidget *parent) :
    QWidget(parent),
    ui(new Ui::OneWidget)
{
    ui->setupUi(this);
    QString word;
    //用不同的ttf文件，显示不一样
    for(int i=0xf000;i<0xf2e0;i++){   //2000=2b60  f000-f1000
      QChar a(i);
       word.append(QString("%1 %2\n").arg(QString::number(i,16)).arg((QChar)a));
    }
    ui->textEdit->setText(word);
    ui->textEdit->setReadOnly(true);//设置不可编辑
    QList<int> list={0x6dd,0x6de,0x6e9,0x7f7,0xd60,0xdf4,0xf13,0xf15,0xf17,0xfbe,0xfc2,0xfc3,0xfc4,0x23e9,0x23f0,0x23f3,
                    0x23f8,0x3299,0x329a,0x2b50,0x2b55,0x2b05,0x2b07,0x29bf,0x2934,0x2935,
                    0x27b0,0x27b3,0x27bd,0x2794,0x2795,0x2796,0x2797,0x2764,0x2766,0x2756,0x274e,0x274b,0x2746,0x2728,
                    0x270d,0x2710,0x270a,0x2702,0x26f5,0x26d1,0x2681,0x267f,0x2709};

    ui->pushButton->setText(QChar(0xf015));
}

OneWidget::~OneWidget()
{
    delete ui;
}
